function language_code(language) {
	// wiki {{Language icon}} use ISO 639.
	// OK: {{Language icon|zh-Hant}}
	// NG: {{Language icon|cmn-Hant}}
	var code = CeL.language_tag.language_code(language, true);
	if (code)
		code = code.replace(/^cmn-/, 'zh-');
	return code;
}

function check_quote(text, messages, title) {
	var count = text.count_of('（') - text.count_of('）');

	if (count) {
		CeL.debug('[' + text + ']: diff: ' + count);
		if (count > 0) {
			if (text.startsWith('（'.repeat(count)))
				return text.slice(count);

		} else {
			if (text.endsWith('）'.repeat(-count)))
				return text.slice(0, count);
		}
		messages.add('括弧未對齊：[' + text + ']', title);
		CeL.warn('括弧未對齊(' + count + ')：[' + text + ']');
	}

	return text;
}

function trim_配音(text) {
	// 須注意不可將 "||...}}" → "|...}}"
	return text.trim().replace(PATTERN_null_template_end, '$1');
}

function replace_配音員(配音_data, messages, title, all_matched, region, 配音員) {
	var region_code = CeL.language_tag.region_code(region, true);

	if (!region_code && region.toLowerCase() === 'yue'
		// e.g., [{{language icon|yue|港}}
		&& all_matched.match(/|yue|港}}/i))
		region_code = '香港粵語';
	CeL.debug(('[' + all_matched + ']: ' + region + ' ('
	//
	+ (region_code || ('無法辨識, ' + CeL.language_tag.region_code(region)))
			+ '): ' + 配音員).replace(/</g, '&lt;'));

	if (!region_code && /版$/.test(region))
		region_code = CeL.language_tag.region_code(region.slice(0, -1), true);
	if (!region_code) {
		if (!配音_data.警告[region]++)
			配音_data.警告[region] = 1;
		return all_matched;
	}

	配音員 = check_quote(trim_配音(配音員), messages, title);

	if (region_code in 配音_data) {
		CeL.warn('重複設定配音員 @ 地區['+region+']：' + 配音_data[region_code] + '，' + 配音員);
		// '、'
		配音_data[region_code] += '，' + 配音員;
	} else
		配音_data[region_code] = 配音員;

	return '';
}

function Language_icon(all_matched, language, link) {
	var code = language_code(language);
	if (code) {
		// 統一將 link 置後。
		return '{{Language icon|' + code
				+ (code === language ? '' : '|' + language) + '}} ' + link;
	}
}

function replace_配音(content, title, messages, page) {
	var count = 0,
		count_line = 0,
		possible_misuse;

	content = content.replace(PATTERN_配音_token,
		function(line, before, data, new_line) {
			if (!PATTERN_Language_icon.test(data))
			// 管轄外。
				return line;
			if (PATTERN_配音_mark.test(data))
				throw new Error('此行似乎不僅有單一配音資料：[<nowiki>' + line + '</nowiki>]');

			// e.g., CV：... 演：...
			CeL.debug('決定 prefix', 3, 'replace_配音');
			var _count = data.match(/\s([^\s]{1,3})：/),
				after,
				// https://en.wikipedia.org/wiki/Help:Template#Problems_and_workarounds
				// If the first included character of a template or parser function is one of four wiki markup characters :;*#
				prefix = before.match(/^([:;*#]+)\s*({{)?/);
			if (prefix) {
				prefix = (prefix[1].replace(/;/g, '').replace(/[*#]/g, ':')
					// 當有 prefix[2]，估計為 {{Nihongo}} 之後時，則多加個 ':'。
					// 否則前面沒東西，不多加，直接沿用原先之 prefix[1]。
					+ (prefix[2] ? ':' : '') + ' ')
					// 僅剩 /^\s+$/ 頗怪。
					.replace(/^\s+/g, '');
			} else
				prefix = ': ';
			CeL.debug('prefix: <span style="color:#52f;">' + prefix + '</span>', 3, 'replace_配音');

			if (_count) {
				after = data.slice(_count.index);
				data = data.slice(0, _count.index).trim();
			} else
				after = '';
			_count = 0;

			// TODO: {{Lang-en}} → 'en' @ 配音 template
			CeL.debug('將 "{{' + Template_name + '|lang}} 配音員" → { lang : 配音員 }: <span style="color:#52f;">' + data + '</span>', 3, 'replace_配音');
			var region_code, 配音_code = [],
				//
				配音_data = {
					警告: {}
				},
				replaced = data.replace(PATTERN_配音置後, function(all_matched, lang, 配音員) {
					配音員 = replace_配音員(配音_data, messages, title, all_matched, lang, 配音員);
					if (配音員 !== all_matched)
						_count++;
					return 配音員;
				});

			// 稍作檢測以加快速度。
			if (/{{Lang/i.test(replaced)) {
				CeL.debug('將 "配音員 {{' + Template_name + '|lang}}" → { lang : 配音員 }: <span style="color:#52f;">' + replaced + '</span>', 3, 'replace_配音');
				// console.log(replaced);
				// console.log(PATTERN_配音置前);
				replaced = replaced.replace(PATTERN_配音置前, function(all_matched, 配音員, lang) {
					//console.log(all_matched);
					配音員 = replace_配音員(配音_data, messages, title, all_matched, lang, 配音員);
					if (配音員 !== all_matched)
						_count++;
					return 配音員;
				});
			}

			if (replaced === data) {
				CeL.debug('將 data 視作 default_region 之資料: <span style="color:#52f;">' + replaced + '</span>', 3, 'replace_配音');
				if (region_code = all_plain_text(replaced)) {
					// e.g., 聲優：[[田中理惠]]
					配音_data[default_region] = check_quote(trim_配音(region_code[1]), messages);
					replaced = region_code[2] || '';
				} else {
					CeL.warn('無法解析：[' + line + ']');
					return line;
				}
			}

			CeL.debug('去除無用之字元與模板參數: <span style="color:#52f;">' + replaced + '</span>', 3, 'replace_配音');
			replaced = trim_配音(replaced)
				// for {{Nihongo}}
				.replace(/\s*\|\s*extra2\s*=\s*}}/, '');

			CeL.debug('去除無意義的符號: <span style="color:#52f;">' + replaced + '</span>', 3, 'replace_配音');
			replaced = replaced.replace(/[\s\|!！;；，、／。)）]+$/, '');
			if (/^[\s\|!！;；，、／。\[\]()（）]+$/.test(replaced))
			// 僅存無意義的符號、無用之字元。通常是因為無法匹配的 "[]"、"（）"。
			// e.g., "；]"
				replaced = '';

			if (replaced && replaced !== '}}') {
				CeL.debug('處理剩餘 data: <span style="color:#52f;">' + replaced + '</span>', 3, 'replace_配音');
				if (!配音_data[default_region]
					// e.g., {{nihongo|配音：[[悠木碧]]／...}} → { ja : [[悠木碧]] }
					&& (region_code = all_plain_text(replaced))) {
					配音_data[default_region] = region_code[1];
					replaced = region_code[2] || '';
				} else {
					// 對於無法解析或有問題者，將跳過是行不處理。
					// {{color|blue|~~}}
					if (false)
						messages.add('尚有剩餘 [<b style="color:blue;"><nowiki>' + replaced + '</nowiki></b>] 未處理，跳過此行！', title);
					CeL.warn('尚有剩餘 [<b style="color:blue;">' + replaced + '</b>]，跳過此行：[<b style="color:blue;">' + line + '</b>]');
					possible_misuse = true;
					return line;
				}
			}

			for (region_code in 配音_data.警告)
				messages.add('無法辨識之國家/區域：[' + region_code + ']（出現' + 配音_data.警告[region_code] + '次）', title);
			delete 配音_data.警告;
			// 把預設語系放最前面。
			if (配音_data[region_code = default_region]) {
				配音_code.push(region_code + LANG_SEPARATOR + 配音_data[region_code]);
				delete 配音_data[default_region];
			}
			for (region_code in 配音_data)
				配音_code.push(region_code + LANG_SEPARATOR + 配音_data[region_code]);

			// '（(': e.g., * '''奧利維‧波蘭傑 '''（聲優：南央美）
			before = before.replace(/[\|\s，、；／（(]+$/, '');

			CeL.debug('生成置換結果: <span style="color:#52f;">' + replaced + '</span>' + after, 3, 'replace_配音');
			// e.g., ': 聲 - ...'
			if (/^[:;*#]*\s*\n$/.test(before += replaced + after + new_line))
				before = '';

			count += _count;
			count_line++;

			CeL.debug('return: <span style="color:#52f;">' + before + '</span>[' + prefix + ']', 3, 'replace_配音');
			return before + (配音_code.length ? prefix + '{{' + 配音_template_name + '|' + 配音_code.join('|') + '}}' + new_line : '');
		})

	// {{languageicon|日}}{{lang|ja|ルルーシュ}}
	// →
	// {{lang-ja|ルルーシュ}}
	.replace(
		/{{Language *icon\s*\|\s*([^\[\]{};]+)\s*}}\s*{{lang(?:-|\s*\|\s*)([a-z]{2,4})\s*\|\s*([^\|{}]+)}}/gi,
		function($0, $1, $2, $3) {
			CeL.debug('{{lang-code|...}}: ' + [$0, $1, $2, $3].join(' ; '));
			if (language_code($1) === ($2 = language_code($2)))
				return '{{lang-' + $2 + '|' + $3 + '}}';
			// 無法解析。
			return $0;
		});

	if (count_line > 0)
	// 削減了
		messages.add('將' + count_line + '行中，共' + count + '個{{tlx|' + Template_name + '}}以{{tlx|' + 配音_template_name + '}}表達。', title);

	// 這是可以不要做的改變。see [[Special:Diff/34457779|欧洲联盟]]
	//content = trim_配音(content);

	count = 0;
	content = content.replace(
		PATTERN_Language_icon_link,
		function(all_matched, language, link) {
			var wiki_code = Language_icon(all_matched, language, link);
			if (wiki_code)
				count++;
			return wiki_code || all_matched;
		}).replace(
		PATTERN_Language_icon_link_front,
		function(all_matched, link, language) {
			var wiki_code = Language_icon(all_matched, language, link);
			if (wiki_code)
				count++;
			return wiki_code || all_matched;
		});
	if (count > 0)
		messages.add('處理了' + count + '個用法正確，但語系代碼錯誤或可簡化的{{tlx|' + Template_name + '}}。', title);

	if (possible_misuse || PATTERN_invalid_Language_icon.test(content)) {
		// 遺留 possible misuse。
		possible_misuse_list.push('* [[' + title + ']]');
		messages.add('尚存有可能誤用的{{tlx|' + Template_name + '}}未處理！', title);
	}

	return content;
}

